// Get battery icon based on percentage
const unsigned char *getBatteryIcon(int percentage) {
  if (percentage >= 75)
    return icon_battery_full;
  if (percentage >= 40)
    return icon_battery_med;
  if (percentage >= 15)
    return icon_battery_low;
  return icon_battery_empty;
}

